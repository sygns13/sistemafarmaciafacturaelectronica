   public void cargarUsuarios() {
                String text = jTextField1.getText();
                control.Sql = "select u.idUsuario, u.nomusr, t.nomtpus,  d.dni, concat(d.nom,' ',d.ape) as nombre \n" +
"  from usuario u \n" +
" inner join tipousuario t on t.idtipousuario=u.idtipousuario \n" +
" inner join datosusuario d on u.idusuario=d.Usuario_idusuario WHERE concat(d.nom,' ',d.ape) LIKE '%" + text + "%' or d.ape LIKE '%" + text + "%' or d.nom LIKE '%" + text + "%' or d.dni like '%" + text + "%' order by d.ape;";                
                control.LlenarJTabla(model, control.Sql, 5);
        }

            private void seleccionarusuario() {
                int row = tblVendedores.getSelectedRow();
                datos = new String[5];
                datos[0] = model.getValueAt(row, 0).toString();//id
                datos[1] = model.getValueAt(row, 1).toString();//dniRuc
                datos[2] = model.getValueAt(row, 2).toString();//Nombre
                datos[3] = model.getValueAt(row, 3).toString();//direcciónd 
                datos[4] = model.getValueAt(row, 4).toString();//direcciónd

                this.dispose();

        }

        /**
         *
         * @return [id, dniRuc, nombre, dirección]
         */
        public String[] getDatos() {
                return datos;
        }

