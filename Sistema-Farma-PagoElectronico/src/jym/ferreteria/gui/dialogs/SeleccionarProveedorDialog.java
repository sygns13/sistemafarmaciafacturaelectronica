    private void cargarProveedores() {
        String text = txtBuscar.getText();
        control.Sql = "SELECT `idProveedor`, `RUC`, `nombre`, `direccion` FROM `proveedor` "
                + "WHERE `idProveedor`<>0 AND (`RUC` LIKE '%" + text + "%' OR `nombre` LIKE '%" + text + "%') ORDER BY `nombre`;";
        control.LlenarJTabla(model, control.Sql, 4);
    }

    private void seleccionarProveedor() {
        int row = tablaProveedores.getSelectedRow();
        datos = new String[4];
        datos[0] = model.getValueAt(row, 0).toString();//id
        datos[1] = model.getValueAt(row, 1).toString();//ruc
        datos[2] = model.getValueAt(row, 2).toString();//nombre
        datos[3] = model.getValueAt(row, 3).toString();//direccci√≥n
        this.dispose();

    }

    /**
     *
     * @return [id, ruc, nombre, direcci√≥n]
     */
    public String[] getDatos() {
        return datos;
    }

