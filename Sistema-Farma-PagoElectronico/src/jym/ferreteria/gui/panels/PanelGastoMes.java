    public void setPeriodo(int year, int month) {

        FrmRegistrarGastos.control.Sql = "SELECT g.`idGastos` , c.`nombre`, co.`nombre`, CONCAT(diaIndice(WEEKDAY(g.`fecha`)),' ',date_format(g.`fecha`,'%d/%m/%Y')), g.`detalle`, format(g.`importe`,2) FROM clasificaciongastos c, conceptogastos co, gastos g WHERE c.`idClasificacionGastos`=co.`idClasificacionGastos` AND co.`idConceptoGastos`=g.`idConceptoGastos` AND g.`anio`='" + year + "' AND g.`mes`='" + month + "';";
        System.out.println(FrmRegistrarGastos.control.Sql);
        FrmRegistrarGastos.control.LlenarJTabla(model, FrmRegistrarGastos.control.Sql, 6);
    }

